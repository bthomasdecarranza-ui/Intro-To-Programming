# int() function

# Example of calling the int() function
age = int("25")

# Name: int
# Arguments: A number or string that represents an integer
# Side Effects: Converts the value into an integer type
# Return Value: The integer version of the provided value


# str() function

# Example of calling the str() function
text = str(123)

# Name: str
# Arguments: Any value (number, boolean, object, etc.)
# Side Effects: Converts the value into a string
# Return Value: The string representation of the value


# bool() function

# Example of calling the bool() function
result = bool(1)

# Name: bool
# Arguments: Any value
# Side Effects: Evaluates the truthiness of the value
# Return Value: True or False depending on the value


# float() function

# Example of calling the float() function
price = float("19.99")

# Name: float
# Arguments: A number or string representing a decimal number
# Side Effects: Converts the value into a floating-point number
# Return Value: The floating-point version of the provided value


# len() function

# Example of calling the len() function
length = len("Berklee")

# Name: len
# Arguments: A sequence (string, list, tuple, etc.)
# Side Effects: None
# Return Value: The number of items in the sequence


# pow() function

# Example of calling the pow() function
power_value = pow(2, 3)

# Name: pow
# Arguments: Two numbers (base and exponent)
# Side Effects: None
# Return Value: The base raised to the power of the exponent


# abs() function

# Example of calling the abs() function
distance = abs(-15)

# Name: abs
# Arguments: A numeric value
# Side Effects: None
# Return Value: The absolute (non-negative) value of the number